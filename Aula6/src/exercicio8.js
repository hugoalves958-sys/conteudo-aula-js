// Exercicío 8: Escreva um algoritmo para ler as dimensões de um triângulo (base e altura), calcular e escrever a área do Triângulo.

alert("Triângulo");

let base = parseFloat(prompt("Informe o valor da base: "));

let altura = parseFloat(prompt("Informe o valor da altura"));

let resultado = (base * altura) /2;

alert(`Área do Triângulo é:  ${resultado}`);

